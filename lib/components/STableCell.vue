<script setup lang="ts">
import { computed } from 'vue'
import { TableCell } from '../composables/Table'
import STableCellAvatar from './STableCellAvatar.vue'
import STableCellAvatars from './STableCellAvatars.vue'
import STableCellDay from './STableCellDay.vue'
import STableCellPill from './STableCellPill.vue'
import STableCellText from './STableCellText.vue'

const props = defineProps<{
  name: string
  className?: string
  cell?: TableCell | ((value: any, record: any) => TableCell)
  value: any
  record: any
  records: Record<string, any>
}>()

const cell = computed(() =>
  typeof props.cell === 'function'
    ? props.cell(props.value, props.record)
    : props.cell
)
</script>

<template>
  <div class="STableCell" :class="[className, `col-${name}`]">
    <STableCellText
      v-if="!cell || cell.type === 'text'"
      :value="value"
      :record="record"
      :icon="cell?.icon"
      :getter="cell?.value"
      :link="cell?.link"
      :color="cell?.color"
      :icon-color="cell?.iconColor"
      :on-click="cell?.onClick"
    />
    <STableCellDay
      v-else-if="cell.type === 'day'"
      :value="value"
      :record="record"
      :format="cell.format"
      :color="cell.color"
    />
    <STableCellPill
      v-else-if="cell.type === 'pill'"
      :value="value"
      :record="record"
      :getter="cell.value"
      :color="cell.color"
    />
    <STableCellAvatar
      v-else-if="cell.type === 'avatar'"
      :value="value"
      :record="record"
      :image="cell.image"
      :name="cell.name"
      :link="cell.link"
      :color="cell.color"
    />
    <STableCellAvatars
      v-else-if="cell.type === 'avatars'"
      :value="value"
      :record="record"
      :avatars="cell.avatars"
      :color="cell.color"
    />
    <component
      v-else-if="cell.type === 'component'"
      :is="cell.component"
      :value="value"
      :record="record"
      v-bind="cell.props"
    />
  </div>
</template>

